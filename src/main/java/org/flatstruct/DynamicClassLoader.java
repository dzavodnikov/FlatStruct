package org.flatstruct;

/**
 * Load created class.
 */
public class DynamicClassLoader extends ClassLoader {

    public Class<?> defineClass(final String name, final byte[] bytes) {
        return defineClass(name, bytes, 0, bytes.length);
    }
}
